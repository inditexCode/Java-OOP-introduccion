package m1_clases_Metodos;
/*
   Una CLASE es un modelo o plantilla que define las propiedades (atributos) 
y comportamientos (métodos) de un tipo específico de objeto. En términos simples, 
es una abstracción que describe qué características y funcionalidades tendrá un objeto.

Características clave de las clases:

Son la base para crear objetos.
Contienen atributos (variables) que describen las propiedades de un objeto.
Incluyen métodos (funciones) que definen los comportamientos de un objeto.
Ejemplo de una clase:
Una clase Coche podría tener:

Atributos: marca, modelo, color.
Métodos: arrancar(), detener().
---------------------------------------------------------------------------------------- 
 * */
 