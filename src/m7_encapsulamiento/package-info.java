package m7_encapsulamiento;

/*
 *Encapsulamiento en Java
El encapsulamiento es un pilar fundamental de la programación orientada a objetos (POO) 
que consiste en restringir el acceso directo a los atributos de una clase y proporcionar 
métodos controlados para leer o modificar su estado. Esto se logra mediante la 
declaración de atributos como privados y el uso de métodos públicos llamados 
getters y setters para acceder a ellos.

Principales características del encapsulamiento
Control de acceso:

Los atributos de una clase se declaran como private, evitando que otras clases accedan 
directamente a ellos.
Los métodos public actúan como intermediarios seguros para interactuar con los datos.
Protección de datos:

Ayuda a prevenir modificaciones indebidas o no deseadas.
Asegura que los valores sean validados antes de ser asignados.
Facilidad de mantenimiento:

Si necesitas cambiar la implementación interna de una clase, no afecta al resto del código
mientras los métodos públicos se mantengan igual.
Flexibilidad:

Puedes agregar reglas personalizadas (como validaciones o transformaciones) dentro de los
getters o setters. 
 * */
 