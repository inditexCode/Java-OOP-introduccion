package m4_herencias;

/*
 * Explicación de la Herencia en Programación Orientada a Objetos
La herencia es un principio fundamental de la programación orientada a objetos (POO) 
que permite crear una nueva clase (clase hija o derivada) a partir de una clase existente 
(clase padre o base). La clase hija hereda los atributos y métodos de la clase padre, 
lo que permite reutilizar el código y facilitar su mantenimiento y extensión.

¿Qué hace la herencia?
Reutilización de código:

La clase hija puede usar los atributos y métodos definidos en la clase padre 
sin necesidad de volver a escribirlos.

Extensibilidad:

La clase hija puede añadir nuevos atributos o métodos específicos, además de los heredados.
Modularidad:

El diseño es más claro y fácil de mantener porque las funcionalidades comunes se colocan en una clase base.
Polimorfismo:

Los objetos de la clase hija pueden ser tratados como objetos de la clase base, 
lo que permite escribir código más genérico y flexible.
 
 * */
 